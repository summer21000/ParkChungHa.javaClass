package com.example.chap01;

public class IntegerValues {
    public static void main(String[] args) {
        byte a = Byte.MAX_VALUE;
        short b = Byte.MIN_VALUE;
        int c = 102;
        long d = 103;

        System.out.print("a는 " + a + ", ");
        System.out.print("b는 " + b + ", ");
        System.out.println("c는 " + c);
        System.out.println("byte d의 값은 " + d);
    }
}
